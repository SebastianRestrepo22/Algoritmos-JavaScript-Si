// 14. Desarrollar un algoritmo que evalué la siguiente expresión aritmética 1/n.

let n = parseInt(prompt("Ingrese el valor en n: "))

Resultado = 1/n

if (n !== 0){
    console.log("El resultado de 1/n es: ", Resultado)
}
else{
    console.log("Datos invalidos.")
}

